import { existsSync, writeFileSync } from "node:fs";
import { generateKeyPairSync } from "node:crypto";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const envPath = resolve(__dirname, "../.env");
const force = process.argv.includes("--force");

if (existsSync(envPath) && !force) {
  console.error(`${envPath} already exists; not overwriting.`);
  console.error("Re-run with --force to replace it.");
  process.exit(1);
}

const { privateKey, publicKey } = generateKeyPairSync("ec", {
  namedCurve: "P-256",
});

const privateKeyPem = privateKey.export({
  type: "pkcs8",
  format: "pem",
});
const publicJwk = publicKey.export({ format: "jwk" });
const jwk = {
  kty: publicJwk.kty,
  x: publicJwk.x,
  y: publicJwk.y,
  crv: publicJwk.crv,
  use: "sig",
  alg: "ES256",
  kid: "demo-key-1",
};
const jwks = {
  keys: [jwk],
};
const jwksDataUrl = `data:application/json;base64,${Buffer.from(
  JSON.stringify(jwks),
).toString("base64")}`;
const escapedPrivateKey = privateKeyPem.replace(/\n/g, "\\n");

const envContents = `# Generated by scripts/generate-demo-jwt-keypair.mjs.
DEMO_PRIVATE_KEY="${escapedPrivateKey}"
DEMO_JWKS="${jwksDataUrl}"

# Optional comma-separated web origins allowed to call Better Auth routes.
# Local Flutter web origins on localhost/127.0.0.1 are accepted automatically.
BETTER_AUTH_TRUSTED_ORIGINS=https://your-web-demo.example
`;

writeFileSync(envPath, envContents);
console.error(`Wrote ${envPath}`);

console.error("");
console.error("Configure the matching public JWKS in Convex:");
console.error(`npx convex env set DEMO_JWKS '${jwksDataUrl}'`);
console.error("");
console.error("DEMO_PRIVATE_KEY for local .env:");
process.stdout.write(`${escapedPrivateKey}\n`);
